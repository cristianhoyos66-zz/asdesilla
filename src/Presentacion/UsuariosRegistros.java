    /**
     * Búsqueda de los usuarios coincidentes según un carácter ingresado
     * @param evt 
     */
    private void btnBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusquedaActionPerformed
        if(txtDocUsu.getText() == null || txtDocUsu.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe ingresar por lo menos un carácter del número de identificación del usuario", "Gestión Asdesilla", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                jcUsuario.removeAllItems();
                p = OpUsuarios.obtenerUsuarios(txtDocUsu.getText());
                Iterator it = p.keySet().iterator();
                if (p.size()>0) {
                    //JOptionPane.showMessageDialog(null, "Se han encontrado coincidencias");
                }else{
                    //JOptionPane.showMessageDialog(null, "No se han encontrado coincidencias");
                }
                jcUsuario.addItem("Seleccione");
                while(it.hasNext()){
                    jcUsuario.addItem(p.get(it.next()));
                }
                //txtDocUsu.setText("");
            } catch (SQLException ex) {
                Logger.getLogger(UsuariosRegistros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnBusquedaActionPerformed

