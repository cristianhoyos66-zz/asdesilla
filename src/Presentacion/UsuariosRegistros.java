    /**
     * Método que permite buscar un registro de un usuario de acuerdo al número de identificación digitado
     * y muestra la información obtenida en la tabla de registros.
     */
    public void buscar() {
        String id;
        ArrayList <Object[]> l;
        id = null;
        
        /*if(id.equals("Seleccione")){
            id = null;
        }*/
        
        if (!"Seleccione".equals(jcUsuario.getSelectedItem().toString())) {
            for (Map.Entry<String, String> usuarios : p.entrySet()){
                String u = usuarios.getKey();
                if (jcUsuario.getSelectedItem().toString().equals(usuarios.getValue())) {
                    id = u;
                }
            }
        }
        
        if(id == null){
            JOptionPane.showMessageDialog(null, "Debe seleccionar un usuario a buscar", "Gestión Asdesilla", JOptionPane.ERROR_MESSAGE);
        }else{
            l = OpUsuarios.buscarRegistros(id);
            if(l.size()>0){
                btnDetalles.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnEliminar.setText("Deshabilitar");
                btnEliminar.setIcon(new ImageIcon(getClass().getResource("/Imagenes/disable-icon.png")));
                dtm = (DefaultTableModel) Registros.getModel();
                if(dtm.getRowCount()!=0){
                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        dtm.removeRow(i);
                        i-=1;
                    }
                }
                try{
                    for (int i = 0; i < l.size(); i++) {
                        dtm.addRow(l.get(i));
                    }
                }catch(Exception e){
                    System.out.println("Error: "+e);
                }
            }
        }
    }
    
    /**
    * Método que permite cambiar el estado de un usuario registrado 
    * de acuerdo a la identificación digitada.
    */
    public void cambiarEstado(){
        String identificacion = Registros.getValueAt(Registros.getSelectedRow(), 0).toString();
        if(identificacion == null || identificacion.trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe seleccionar el registro a cambiar el estado");
        }

        if(!identificacion.trim().isEmpty()){
            JOptionPane x = new JOptionPane();
            if(x.showConfirmDialog(null,"Está seguro que desea cambiar el estado de este registro?")==0){
                OpUsuarios.cambiarEstado(identificacion);
                jcUsuario.removeAllItems();
                jcUsuario.addItem("Seleccione");
                btnDetalles.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnEliminar.setText("Deshabilitar");
                btnEliminar.setIcon(new ImageIcon(getClass().getResource("/Imagenes/disable-icon.png")));
                dtm = (DefaultTableModel) Registros.getModel();
                if(dtm.getRowCount()!=0){
                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        dtm.removeRow(i);
                        i-=1;
                    }
                }
                try{
                    ArrayList <Object[]> l = OpUsuarios.obtenerRegistros();
                    for (int i = 0; i < l.size(); i++) {
                        dtm.addRow(l.get(i));
                    }
                }catch(Exception e){
                    System.out.println("Error: "+e);
                }
            }
        }
    }
