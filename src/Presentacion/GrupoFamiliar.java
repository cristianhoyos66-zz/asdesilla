    /**
     * Método que permite guardar la información de un grupo familiar
     * @param evt 
     */
    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNGrFamiliar.getText();
        if (nombre == null || nombre.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe diligenciar el nombre del grupo familiar a registrar", "Gestión Asdesilla", JOptionPane.ERROR_MESSAGE);
        }else{
            OpFamiliares.guardarGrupoFamiliar(nombre);
            btnCrear.setVisible(false);
            txtNGrFamiliar.setVisible(false);
            txtNGrFamiliar.setText("");
            lblNGrFamiliar.setVisible(false);
            jcGrFamiliar.removeAllItems();
            jcGrFamiliar.addItem("Seleccione");
        }
    }//GEN-LAST:event_btnCrearActionPerformed
