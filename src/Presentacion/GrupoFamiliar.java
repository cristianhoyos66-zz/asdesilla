        if (!jcGrFamiliar.getSelectedItem().equals("Seleccione")) {
            Familiares.txtGrFamiliar.setText(jcGrFamiliar.getSelectedItem().toString());
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un grupo familiar", "Gestión Asdesilla", JOptionPane.ERROR_MESSAGE);
        }
